#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*(s++))
		if (*(s - 1) == (char)c)
			return ((char *)(s - 1));
	if (*(s - 1) == (char)c)
		return ((char *)(s - 1));
	return (NULL);
}
